
class Lesson06_02 {

    public static void main(String[] args) {
        int x = 10;
        int y = 2;
        int z = 10;

        System.err.println(x > y);
        System.err.println(x < y);
        System.err.println(x >= y);
        System.err.println(x <= y);
        System.err.println(x >= z);
        System.err.println(x <= z);

        System.err.println(x == y);
        System.err.println(x != y);
        System.err.println(x == z);
        System.err.println(x != z);

    }
}
